     */
    public function index(Request $request)
    {
        $this->authorize('view-employees');

        $user = $request->user();

        $employees = $user->role === 'admin' ? Employee::all() : Employee::where(['company_id' => $user->company->id])->get();

        return response()->json($employees);
    }

